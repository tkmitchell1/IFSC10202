# < less — the condition is true if left side is less than right side.
# > greater — the condition is true if left side is greater than right side.
# <= less or equal.
# >= greater or equal.
# == equal.
# != not equal.

x = 10
y = 20
if 2 * x != y:
    print("x and y don't make 1:2 ratio")
else:
    print("x and y make 1:2 ratio")